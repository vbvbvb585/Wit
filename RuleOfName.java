public class RuleOfName {
    public static void main(String[] args) {
        // สามารถตัวเลข ตัวอักษร และเครื่องหมาย _ $
        // การประกาศตัวแปร => ชนิดตัวแปร   ชื่่อตัวแปร = ค่าของข้อมูล;
        int num$01 = 10; 
        System.out.println(num$01);

        // ห้ามซ้ำกับคำสงวน 
        // int public = 5;
        // int boolean = 6;

        // case sentitive
        int Num1 = 15;
        int num1 = 50;
        System.out.println(Num1);
        System.out.println(num1);


        int a , b , c , d ;
        
    }
}
