package Week11;

public class WhileLoop_01 {
    public static void main(String[] args) {
        // ค่าเริ่มต้น
        //เงื่อนไข
        //update รอบ
        int i  =2; // ค่าเริ่มต้น
        while (i <=20) { //เงื่อนไข
            System.out.println( i++); // ค่าที่ต้องการแสดงผล
            i ++; // update รอบ
        }
    }
}